export function getCubeWireframeVertices(cubeSize: number): RawVertexBuffer {

  const h = cubeSize / 2

  return [

    // Front square
    [[-h, -h, h], [h, -h, h]],
    [[-h, h, h], [-h, -h, h]],  
    [[h, h, h], [h, -h, h]],

    // Back square
    [[-h, -h, -h], [-h, h, -h]],
    [[h, -h, -h], [-h, -h, -h]],
    [[h, h, -h], [h, -h, -h]],

    // Left square
    [[-h, h, h], [-h, h, -h]], 
    [[-h, -h, h], [-h, h, h]],
    [[-h, -h, -h], [-h, -h, h]],

    // Right square
    [[h, h, h], [h, h, -h]],
    [[h, -h, h], [h, h, h]],
    [[h, -h, -h], [h, -h, h]],  

    // Top square
    [[-h, h, h], [h, h, h]],
    [[h, h, -h], [-h, h, -h]],

    // Bottom square 
    [[-h, -h, h], [h, -h, h]],
    [[-h, -h, -h], [h, -h, -h]]

  ]

}
